                <h3>Peso muerto (Deadlift)</h3>
                <p>Este ejercicio trabaja los músculos de los glúteos, los isquiotibiales y la espalda baja. Colócate de pie con los pies separados al ancho de los hombros y sostén una barra o mancuernas frente a ti. Flexiona las rodillas ligeramente, mantén la espalda recta y baja la parte superior del cuerpo hacia el suelo. Luego, vuelve a la posición inicial levantando la parte superior del cuerpo.</p>
