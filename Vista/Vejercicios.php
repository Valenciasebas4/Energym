        <h2>Ejercicios de Cardio</h2>
            <div class="col-md-4">
                <h3>Burpees</h3>
                <p>Este ejercicio de cuerpo completo es ideal para elevar la frecuencia cardíaca. Comienza en posición de pie, luego agáchate, coloca las manos en el suelo, salta hacia atrás en posición de plancha, haz una flexión, salta hacia adelante y finalmente salta hacia arriba con los brazos extendidos.</p>
            </div>
            <div class="col-md-4">
                <h3>Saltar la cuerda</h3>
                <p>Un ejercicio de cardio efectivo y divertido. Puedes saltar la cuerda en casa o en el gimnasio.</p>
            </div>
            <div class="col-md-4">
                <h3>Ciclismo</h3>
                <p>Ya sea en una bicicleta estática o al aire libre, el ciclismo es una excelente opción para el cardio. Puedes ajustar la resistencia para aumentar la intensidad.</p>
            </div>
            <div class="col-md-4">
                <h3>Saltos de tijera (Jumping Jacks)</h3>
                <p> Un ejercicio simple pero efectivo. Comienza con los pies juntos y los brazos a los lados, luego salta y separa los pies mientras levantas los brazos por encima de la cabeza. Vuelve a la posición inicial y repite.</p>
            </div>
