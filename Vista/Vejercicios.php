        <h2>Ejercicios de Abdomen</h2>
            <div class="col-md-4">
                <h3>Crunches (abdominales tradicionales)</h3>
                <p>Acuéstate en el suelo con las piernas flexionadas y los pies apoyados en el suelo. Coloca las manos detrás de la cabeza o cruzadas sobre el pecho. Eleva el torso hacia las rodillas contrayendo los músculos abdominales y luego baja lentamente. Realiza varias repeticiones.</p>
            </div>
            <div class="col-md-4">
                <h3>Plancha frontal</h3>
                <p>Colócate en posición de plancha con los antebrazos apoyados en el suelo y los codos alineados con los hombros. Mantén el cuerpo recto y contrae los abdominales. Aguanta la posición durante 30 segundos o más, según tu nivel de resistencia.</p>
            </div>
            <div class="col-md-4">
                <h3>Plancha lateral</h3>
                <p>Acuéstate de lado, apoyándote sobre un antebrazo y con el codo alineado con el hombro. Levanta las caderas del suelo, manteniendo el cuerpo recto y apoyándote únicamente en el antebrazo y el lateral del pie. Mantén la posición durante 30 segundos y luego cambia de lado.</p>
            </div> 
            <div class="col-md-4">
                <h3>Elevaciones de piernas</h3>
                <p>Acuéstate boca arriba con las manos debajo de los glúteos para mayor soporte. Levanta las piernas estiradas hacia arriba, manteniendo los abdominales contraídos. Baja las piernas lentamente sin tocar el suelo y luego repite el movimiento.</p>
            </div> 
            <div class="col-md-4">
            <img src="../Vista/img/img-ejercicios/prelum.png" >       
            </div> 
            <div class="col-md-4">
                <h3>Russian twists</h3>
                <p>Siéntate en el suelo con las piernas flexionadas y los pies apoyados en el suelo. Inclina ligeramente el torso hacia atrás, manteniendo la espalda recta. Luego, gira el torso hacia un lado y toca el suelo con las manos. Vuelve al centro y repite el movimiento hacia el otro lado. Alterna de un lado a otro.</p>
            </div> 
        </div>
        <hr style="color: black"> 
        <div class="row">
        <h2>Ejercicios de Triceps</h2>
            <div class="col-md-4">
                <h3>Fondos en paralelas</h3>
                <p>Colócate entre dos barras paralelas o apoyándote en dos bancos paralelos. Sujétate con las manos en las barras o en los bordes de los bancos, estira los brazos y levanta los pies del suelo. Flexiona los codos para descender el cuerpo hacia el suelo, manteniendo el torso recto, y luego extiende los brazos para volver a la posición inicial.</p>
            </div>
            <div class="col-md-4">
                <h3>Press de banca con agarre cerrado</h3>
                <p>Acuéstate en un banco plano y sostén una barra con las manos colocadas en un agarre cerrado, a una distancia menor que el ancho de los hombros. Levanta la barra y desciéndela lentamente hasta que casi toque el pecho, manteniendo los codos cerca del cuerpo. Luego, extiende los brazos para volver a la posición inicial.</p>
            </div>
            <div class="col-md-4">
                <h3>Extensiones de tríceps con mancuernas</h3>
                <p>Siéntate en un banco con la espalda recta y sostén una mancuerna con ambas manos por encima de la cabeza. Baja lentamente la mancuerna detrás de la cabeza, flexionando los codos, y luego vuelve a subir extendiendo los brazos.</p>
            </div>
            <div class="col-md-4">
                <h3>Patadas de tríceps</h3>
                <p>Ponte en posición de tabla alta con las manos debajo de los hombros y los pies juntos. Desde esta posición, dobla el brazo derecho y lleva el codo hacia atrás, llevando la mano hacia la cadera derecha. Extiende el brazo derecho hacia atrás y repite el movimiento con el brazo izquierdo. Alterna los brazos.</p>
            </div>
            <div class="col-md-4">
                <img src="../Vista/img/img-ejercicios/triceps.png" >
            </div>
            <div class="col-md-4">
                <h3>Press francés con barra EZ</h3>
                <p>Acuéstate en un banco plano y sostén una barra EZ con las manos en un agarre estrecho. Mantén los brazos extendidos y baja la barra lentamente hacia la frente flexionando los codos. Luego, vuelve a subir la barra extendiendo los brazos.</p>
            </div> 
        </div>
        <hr style="color: black"> 
        <div class="row">
        <h2>Ejercicios de Biceps</h2>
            <div class="col-md-4">
                <h3>Curl de bíceps con mancuernas</h3>
                <p>De pie, sostén una mancuerna en cada mano con las palmas mirando hacia adelante. Mantén los brazos pegados a los costados y los codos cerca del cuerpo. Flexiona los codos para levantar las mancuernas hacia los hombros, manteniendo los antebrazos perpendiculares al suelo. Luego, baja las mancuernas lentamente hasta la posición inicial.</p>
            </div>
            <div class="col-md-4">
                <h3>Curl de bíceps con barra</h3>
                <p>De pie, sostén una barra con las manos en un agarre supino (palmas hacia arriba) y las manos separadas a la anchura de los hombros. Mantén los brazos pegados a los costados y los codos cerca del cuerpo. Flexiona los codos para levantar la barra hacia los hombros, manteniendo los antebrazos perpendiculares al suelo. Luego, baja la barra lentamente hasta la posición inicial.</p>
            </div>
            <div class="col-md-4">
                <h3>Martillo con mancuernas</h3>
                <p>De pie, sostén una mancuerna en cada mano con las palmas mirándose entre sí. Mantén los brazos pegados a los costados y los codos cerca del cuerpo. Flexiona los codos para levantar las mancuernas hacia los hombros, manteniendo las palmas mirándose entre sí durante todo el movimiento. Luego, baja las mancuernas lentamente hasta la posición inicial.</p>
            </div>
            <div class="col-md-4">
                <h3>Curl concentrado</h3>
                <p>Siéntate en un banco con las piernas separadas y los pies apoyados en el suelo. Sujeta una mancuerna con una mano y apoya el codo en el interior del muslo, cerca de la rodilla. Flexiona el codo para levantar la mancuerna hacia el hombro, manteniendo el resto del brazo inmóvil. Luego, baja la mancuerna lentamente hasta la posición inicial y repite el movimiento con la otra mano.</p>
            </div>
            <div class="col-md-4">
                <img src="../Vista/img/img-ejercicios/biceps.png">
            </div>  
            <div class="col-md-4">
                <h3>Dominadas con agarre supino</h3>
                <p>Colócate en una barra de dominadas con las palmas mirando hacia ti (agarre supino) y las manos separadas a la anchura de los hombros. Extiende los brazos y levántate hasta que la barbilla esté por encima de la barra. Controladamente, baja tu cuerpo hasta la posición inicial.</p>
            </div> 
