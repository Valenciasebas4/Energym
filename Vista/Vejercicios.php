        <h2>Ejercicios de Pierna</h2>
            <div class="col-md-4">
                <h3>Sentadillas (Squats)</h3>
                <p>Este ejercicio trabaja los músculos de los glúteos, los cuádriceps y los isquiotibiales. Colócate de pie, con los pies separados al ancho de los hombros. Flexiona las rodillas y baja las caderas como si te fueras a sentar en una silla. Mantén la espalda recta y los talones apoyados en el suelo. Luego, vuelve a la posición inicial.</p>
            </div> 
            <div class="col-md-4">
                <h3>Zancadas (Lunges)</h3>
                <p>Las zancadas son excelentes para fortalecer los cuádriceps y los glúteos. Da un paso hacia adelante con una pierna, flexiona ambas rodillas y baja el cuerpo hasta que la pierna delantera forme un ángulo de 90 grados. Luego, regresa a la posición inicial y repite con la otra pierna.</p>
            </div> 
            <div class="col-md-4">
                <h3>Prensa de piernas (Leg Press)</h3>
                <p>Si tienes acceso a una máquina de prensa de piernas en el gimnasio, este ejercicio es ideal para trabajar los cuádriceps, los glúteos y los isquiotibiales. Ajusta el asiento y la plataforma de acuerdo a tu altura, coloca los pies en la plataforma y empuja hacia afuera mientras mantienes la espalda apoyada en el respaldo.</p>
            </div> 
            <div class="col-md-4">
                <h3>Elevación de talones (Calf Raises)</h3>
                <p>Para fortalecer los músculos de la pantorrilla, ponte de pie con los pies separados al ancho de los hombros. Luego, levanta los talones del suelo, mantén la posición durante unos segundos y luego baja lentamente.</p>
            </div> 
